<template>
  <div class="mod-config">
    <el-form :inline="true" :model="dataForm" @keyup.enter.native="getDataList()">
      <el-form-item>
        <el-input v-model="dataForm.key" clearable placeholder="参数名"></el-input>
      </el-form-item>
      <el-form-item>
        <el-button @click="getDataList()">查询</el-button>
        <el-button
          v-if="isAuth('product:brand:save')"
          type="primary"
          @click="addOrUpdateHandle()"
        >新增
        </el-button>
        <el-button
          v-if="isAuth('product:brand:delete')"
          :disabled="dataListSelections.length <= 0"
          type="danger"
          @click="deleteHandle()"
        >批量删除
        </el-button>
      </el-form-item>
    </el-form>
    <el-table
      v-loading="dataListLoading"
      :data="dataList"
      border
      style="width: 100%;"
      @selection-change="selectionChangeHandle"
    >
      <el-table-column align="center" header-align="center" type="selection" width="50"></el-table-column>
      <el-table-column align="center" header-align="center" label="供用商id" prop="supplierId"></el-table-column>
      <el-table-column align="center" header-align="center" label="供用商名" prop="name"></el-table-column>
      <el-table-column align="center" header-align="center" label="供用商logo" prop="logo">
        <template slot-scope="scope">
          <!-- <el-image
              style="width: 100px; height: 80px"
              :src="scope.row.logo"
          fit="fill"></el-image>-->
          <img :src="scope.row.logo" style="width: 100px; height: 80px"/>
        </template>
      </el-table-column>
      <el-table-column align="center" header-align="center" label="介绍" prop="descript"></el-table-column>
      <el-table-column align="center" header-align="center" label="状态" prop="showStatus">
        <template slot-scope="scope">
          <el-switch
            v-model="scope.row.showStatus"
            :active-value="1"
            :inactive-value="0"
            active-color="#13ce66"
            inactive-color="#ff4949"
            @change="updateBrandStatus(scope.row)"
          ></el-switch>
        </template>
      </el-table-column>

      <el-table-column align="center" header-align="center" label="排序" prop="sort"></el-table-column>
      <el-table-column align="center" fixed="right" header-align="center" label="操作" width="250">
        <template slot-scope="scope">
          <el-button size="small" type="text" @click="updateCatelogHandle(scope.row.supplierId)">关联分区</el-button>
          <el-button size="small" type="text" @click="addOrUpdateHandle(scope.row.supplierId)">修改</el-button>
          <el-button size="small" type="text" @click="deleteHandle(scope.row.supplierId)">删除</el-button>
        </template>
      </el-table-column>
    </el-table>
    <el-pagination
      :current-page="pageIndex"
      :page-size="pageSize"
      :page-sizes="[10, 20, 50, 100]"
      :total="totalPage"
      layout="total, sizes, prev, pager, next, jumper"
      @size-change="sizeChangeHandle"
      @current-change="currentChangeHandle"
    ></el-pagination>
    <!-- 弹窗, 新增 / 修改 -->
    <add-or-update v-if="addOrUpdateVisible" ref="addOrUpdate" @refreshDataList="getDataList"></add-or-update>

    <el-dialog :visible.sync="cateRelationDialogVisible" title="关联分区" width="30%">
      <el-popover v-model="popCatelogSelectVisible" placement="right-end">
        <category-cascader :warelocationPath.sync="warelocationPath"></category-cascader>
        <div style="text-align: right; margin: 0">
          <el-button size="mini" type="text" @click="popCatelogSelectVisible = false">取消</el-button>
          <el-button size="mini" type="primary" @click="addCatelogSelect">确定</el-button>
        </div>
        <el-button slot="reference">新增供用商与库存区的关联</el-button>
      </el-popover>
      <el-table :data="cateRelationTableData" style="width: 100%">
        <el-table-column label="#" prop="id"></el-table-column>
        <el-table-column label="供用商名" prop="supplierName"></el-table-column>
        <el-table-column label="库存分区名" prop="wlName"></el-table-column>
        <el-table-column align="center" fixed="right" header-align="center" label="操作">
          <template slot-scope="scope">
            <el-button
              size="small"
              type="text"
              @click="deleteCateRelationHandle(scope.row.id,scope.row.supplierId)"
            >移除
            </el-button>
          </template>
        </el-table-column>
      </el-table>
      <span slot="footer" class="dialog-footer">
        <el-button @click="cateRelationDialogVisible = false">取 消</el-button>
        <el-button type="primary" @click="cateRelationDialogVisible = false">确 定</el-button>
      </span>
    </el-dialog>
  </div>
</template>

<script>
import AddOrUpdate from './supplier-add-or-update'
import CategoryCascader from '../common/category-cascader'

export default {
  data () {
    return {
      dataForm: {
        key: ''
      },
      supplierId: 0,
      warelocationPath: [],
      dataList: [],
      cateRelationTableData: [],
      pageIndex: 1,
      pageSize: 10,
      totalPage: 0,
      dataListLoading: false,
      dataListSelections: [],
      addOrUpdateVisible: false,
      cateRelationDialogVisible: false,
      popCatelogSelectVisible: false
    }
  },
  components: {
    AddOrUpdate,
    CategoryCascader
  },
  activated () {
    this.getDataList()
  },
  methods: {
    addCatelogSelect () {
      //{"supplierId":1,"wlId":2}
      this.popCatelogSelectVisible = false
      this.$http({
        url: this.$http.adornUrl('/stock/warelocationsupplierrelation/save'),
        method: 'post',
        data: this.$http.adornData({
          supplierId: this.supplierId,
          wlId: this.warelocationPath[this.warelocationPath.length - 1]
        }, false)
      }).then(({data}) => {
        this.getCateRelation()
      })
    },
    deleteCateRelationHandle (id, supplierId) {
      this.$http({
        url: this.$http.adornUrl('/stock/warelocationsupplierrelation/delete'),
        method: 'post',
        data: this.$http.adornData([id], false)
      }).then(({data}) => {
        this.getCateRelation()
      })
    },
    updateCatelogHandle (supplierId) {
      this.cateRelationDialogVisible = true
      this.supplierId = supplierId
      this.getCateRelation()
    },
    getCateRelation () {
      this.$http({
        url: this.$http.adornUrl('/stock/warelocationsupplierrelation/warelocation/list'),
        method: 'get',
        params: this.$http.adornParams({
          supplierId: this.supplierId
        })
      }).then(({data}) => {
        this.cateRelationTableData = data.data
      })
    },
    // 获取数据列表
    getDataList () {
      this.dataListLoading = true
      this.$http({
        url: this.$http.adornUrl('/stock/supplier/list'),
        method: 'get',
        params: this.$http.adornParams({
          page: this.pageIndex,
          limit: this.pageSize,
          key: this.dataForm.key
        })
      }).then(({data}) => {
        if (data && data.code === 0) {
          this.dataList = data.page.list
          this.totalPage = data.page.totalCount
        } else {
          this.dataList = []
          this.totalPage = 0
        }
        this.dataListLoading = false
      })
    },
    updateBrandStatus (data) {
      console.log('最新信息', data)
      let {supplierId, showStatus} = data
      //发送请求修改状态
      this.$http({
        url: this.$http.adornUrl('/stock/supplier/update/status'),
        method: 'post',
        data: this.$http.adornData({supplierId, showStatus}, false)
      }).then(({data}) => {
        this.$message({
          type: 'success',
          message: '状态更新成功'
        })
      })
    },
    // 每页数
    sizeChangeHandle (val) {
      this.pageSize = val
      this.pageIndex = 1
      this.getDataList()
    },
    // 当前页
    currentChangeHandle (val) {
      this.pageIndex = val
      this.getDataList()
    },
    // 多选
    selectionChangeHandle (val) {
      this.dataListSelections = val
    },
    // 新增 / 修改
    addOrUpdateHandle (id) {
      this.addOrUpdateVisible = true
      this.$nextTick(() => {
        this.$refs.addOrUpdate.init(id)
      })
    },
    // 删除
    deleteHandle (id) {
      var ids = id
        ? [id]
        : this.dataListSelections.map(item => {
          return item.supplierId
        })
      this.$confirm(
        `确定对[id=${ids.join(',')}]进行[${id ? '删除' : '批量删除'}]操作?`,
        '提示',
        {
          confirmButtonText: '确定',
          cancelButtonText: '取消',
          type: 'warning'
        }
      ).then(() => {
        this.$http({
          url: this.$http.adornUrl('/stock/supplier/delete'),
          method: 'post',
          data: this.$http.adornData(ids, false)
        }).then(({data}) => {
          if (data && data.code === 0) {
            this.$message({
              message: '操作成功',
              type: 'success',
              duration: 1500,
              onClose: () => {
                this.getDataList()
              }
            })
          } else {
            this.$message.error(data.msg)
          }
        })
      })
    }
  }
}
</script>
